#ifndef TYPE_MACRO_H
#define TYPE_MACRO_H

#ifndef MAX
#define MAX(a, b)   (a >= b ? a : b)
#endif

#ifndef MIN
#define MIN(a, b)   (a <= b ? a : b)
#endif

#endif
